#include"boss.h"
#include"renderer.h"
#include"manager.h"

//=============================================================================
// 敵のコンストラクタ
//=============================================================================
CBoss::CBoss(int nPriority) : CScene2D(PRIORITY_ENEMY)
{
	m_Pos = D3DXVECTOR3(0.0f, 0.0f, 0.0f);
	m_Move = D3DXVECTOR3(0.0f, 0.0f, 0.0f);
	m_Size = D3DXVECTOR2(0.0f, 0.0f);
}


//=============================================================================
// 敵のデストラクタ
//=============================================================================
CBoss::~CBoss()
{

}

//=============================================================================
// 敵の初期化処理
//=============================================================================
HRESULT CBoss::Init(D3DXVECTOR3 pos, D3DXVECTOR3 move, D3DXVECTOR2 Size)
{
	m_Pos = pos;
	m_Move = move;
	m_Size = Size;
	CScene2D::Init(pos, Size);

	return S_OK;
}	// 敵の初期化処理終了



	//=============================================================================
	// 敵の終了処理
	//=============================================================================
void CBoss::Uninit(void)
{
	Release();

}// 敵の終了処理終了


 //=============================================================================
 // 敵の更新処理
 //=============================================================================
void CBoss::Update(void)
{
	CScene2D::SetPosition(m_Pos);
	CScene2D::SetSize(m_Size);
	CScene2D::Update();

}// 敵の更新処理終了



 //=============================================================================
 // 敵の描画処理
 //=============================================================================
void CBoss::Draw(void)
{
	CScene2D::Draw();

}	// 敵の描画処理終了