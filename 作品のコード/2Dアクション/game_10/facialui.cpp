#include "facialui.h"

//=============================================================================
// 顔の差分の表示のコンストラクタ
//=============================================================================
CFacialui::CFacialui(int nPriority) : CScene2D(PRIORITY_UI)
{

}

//=============================================================================
// 顔の差分の表示のデストラクタ
//=============================================================================
CFacialui::~CFacialui()
{

}

//=============================================================================
// 顔の差分の表示の生成処理
//=============================================================================
CFacialui *CFacialui::Create(D3DXVECTOR3 pos, D3DXVECTOR2 Size, int nTex)
{
	CFacialui *pFacialui = NULL;
	pFacialui = new CFacialui;

	if (pFacialui != NULL)
	{
		pFacialui->Init(pos, Size);
		pFacialui->Bindtexture(nTex);
	}
	return pFacialui;
}

//=============================================================================
// 顔の差分の表示の初期化処理
//=============================================================================
HRESULT CFacialui::Init(D3DXVECTOR3 pos, D3DXVECTOR2 Size)
{
	m_Pos = pos;
	CScene2D::Init(pos, Size);
	// アニメーションのテクスチャ設定
	CScene2D::SetTexAnime(0, 0.5f, 0, 1);
	return S_OK;
}

//=============================================================================
// 顔の差分の表示の終了処理
//=============================================================================
void CFacialui::Uninit(void)
{
	//CScene2D::Uninit();
	Release();
}

//=============================================================================
// 顔の差分の表示の更新処理
//=============================================================================
void CFacialui::Update(void)
{
	CScene2D::Update();
}

//=============================================================================
// 顔の差分の表示の描画処理
//=============================================================================
void CFacialui::Draw(void)
{
	CScene2D::Draw();
}