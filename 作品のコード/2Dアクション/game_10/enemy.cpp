void CEnemy::Update(void)
{	
	if (m_Pos.x + m_Size.x >= SCREEN_WIDTH)
	{
		Uninit();
		return;
	}
	else if (m_Pos.x <= -m_Size.x)
	{
		Uninit();
		return;
	}

	if (m_Pos.y + m_Size.y >= SCREEN_HEIGHT)
	{
		Uninit();
		return;
	}
	else if (m_Pos.y <= m_Size.y)
	{
		Uninit();
		return;
	}

	CScene2D::SetPosition(m_Pos);
	CScene2D::SetSize(m_Size);
	CScene2D::Update();

}// “G‚ÌXVˆ—I—¹



