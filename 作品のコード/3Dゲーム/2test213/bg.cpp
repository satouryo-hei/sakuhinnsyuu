//=============================================================================
// 背景の生成処理
//=============================================================================																				
CBg *CBg::Create(D3DXVECTOR3 pos, D3DXVECTOR2 Size,int nTex)
{
	// 背景のポインター生成
	CBg* pBg = NULL;

	// 動的メモリの確保
	pBg = new CBg(PRIORITY_NOMAL);

	// NULLチェック
	if (pBg != NULL)
	{
		// 背景の初期化の呼び出し
		pBg->Init(pos, Size, nTex);
	}
	return pBg;
}

//=============================================================================
// 背景のデフォルト初期化処理
//=============================================================================
HRESULT CBg::Init(D3DXVECTOR3 pos)
{
	return S_OK;
}

//=============================================================================
// 背景の初期化処理
//=============================================================================
HRESULT CBg::Init(D3DXVECTOR3 pos, D3DXVECTOR2 Size,int nTex)
{
	// 背景のデフォルト初期化処理の呼び出し
	Init(pos);

	// 2Dオブジェクトの生成
	m_apScene2D = CScene2D::Creste(pos,Size,nTex);

	return S_OK;
}

//=============================================================================
// 背景の終了処理
//=============================================================================
void CBg::Uninit(void)
{	
	// NULLチェック
	if (m_apScene2D != NULL)
	{
		// 2Dオブジェクトの解放
		m_apScene2D->Uninit();
		m_apScene2D = NULL;
	}

	// 背景の解放
	Release();
}

